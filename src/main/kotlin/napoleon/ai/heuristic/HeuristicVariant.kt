package napoleon.ai.heuristic

// プレイ品質改善の A/B 比較用の挙動セレクタ。
//   BASELINE : 現行の出荷挙動。本番・GUI・回帰テスト・既定値はこちら。
//   CANDIDATE: BASELINE + 検証中の実験。良ければ BASELINE 分岐を削除して無条件化 (= 出荷挙動に昇格) する。
// A/B ハーネス (AiAbCompare) で席0のみ CANDIDATE にし、ロール別の勝率影響を測る運用を想定している。
//
// アクティブな実験: なし (CANDIDATE は次の実験用の予約枠)。新しい実験は CANDIDATE 分岐を足して AiAbCompare で
// 席0効果を測り、良ければ分岐を外して無条件化 (BASELINE に昇格) する運用。
// 過去の実験例: 「2 の死蔵回避」「第1リード献上回避」「能動的セイムリード (切り札の 2 は無条件最優先・サイドは
//              後続フォロー見込み 0.6 以上)」は計測の上で出荷挙動 (BASELINE) に昇格済み
//              (セイムリードは席0で Napoleon +0.81pt / Adjutant +0.32pt / Ally ±0)。
//              「ジョーカーのリード宣言を切り札にして切り札刈り」も昇格済み (席0で Napoleon +2.04pt / 副官・連合 ±0、
//              ジョーカーの第1リードはナポ専用のため副官・連合は不変)。
//              「数札 2 を素の序列で 10 (RANK_T) 相当に格上げ (捨て/温存で後回し・セイムでの最強化を考慮)」も昇格済み
//              (席0で Napoleon +0.25pt / Adjutant +0.07pt / Ally +0.32pt、全ロール正・悪化なし)。
//              「連合フォローの非切り札絵札の積極放出 (後続全員味方の勝ちトリックで温存を解き獲得に変える・小さい絵札
//              から放出)」も昇格済み (席0 Ally +0.37pt、全 CANDIDATE で連合 41.87%→43.49% +1.62pt・獲得 +0.12枚。
//              全席なら強い絵札が手に分散して残り終盤の取りこぼしが減るため小さい絵札からの放出が最良)。
//              「上記放出の isLikelyTeamTakesTrick ガード撤廃 (後続に敵が残っていても、出した後の暫定トップが味方なら
//              側絵札を放出。内側の provisionalLeaderAfter 味方判定のみで制御)」も昇格済み (席0 Ally +0.77pt、
//              全 CANDIDATE で連合 43.49%→44.05% +0.56pt・獲得 +0.014枚。獲得ほぼ横ばいで接戦の取りこぼし減が勝因)。
//              「連合セイム封じ条件の緩和 (AllyFollowPlanner: leaderIsTeammate と trickHonors==0 を撤廃し、味方取り切り
//              or リーダー無敵のときだけ 2 投入を封じる。敵リードや絵札ゼロでも勝ち目があれば 2 で逆転を狙う)」も昇格済み
//              (席0 Ally +0.32pt、全 CANDIDATE で連合 44.05%→44.96% +0.91pt・獲得 +0.076枚。席0より全席の伸びが大きく
//              チーム協調増幅。ナポ軍は NapoleonFollowPlanner 使用のため不変)。
//              「連合の決着絵札積み (AllyFollowPlanner: 味方が取り切るトリックに絵札を 1 枚載せれば連合勝利が確定するなら
//              敵リード奪取と同格の最優先で上積みして決着させる) + 敵リード決着奪取を先頭に並べ替え」も昇格済み
//              (全 CANDIDATE で連合 44.99%→45.07% +0.08pt とほぼ誤差・獲得は微減だが、勝率が上がる以上は正着手として採用。
//              既存の絵札放出/死に絵札上乗せブロックが同手をほぼ選ぶため発火局面は稀)。
//              「ダンプ→副官献上」は -0.70pt と計測され不採用。
//              「連合の敵 void ラフ誘発リード (NapoleonLeadPlanner 同型を AllyLeadPlanner に復活)」は Ally 2万局 −0.05pt /
//              8万局 +0.03pt と符号が振れ実効ゼロで不採用 (旧「連合 void 低札投げ無効化」の未計測分を計測で確定)。ナポ軍では
//              昇格済の同手だが、連合は次リードをナポへ渡すテンポ損が切り札枯らしの確定利得を相殺。上の非 void 優先 −0.17pt と
//              併せ、連合は敵 void を避けても突いても無効と決着 (避ける「ナポ void 回避」も実装利得なしと確認)。
//              「連合の攻撃的リード (ナポ非 void スートの最強非役札投げ)」は Ally -0.62pt と計測され不採用。
//              「連合リードの非 void スート優先 (ナポ非 void へ低札でマストフォロー強制・敵 void ラフ誘発の反転)」は
//              Ally -0.17pt と計測され不採用 (敵 void ラフ誘発の切り札枯らし確定利得が僅差で上回った)。
//              「副官の切り札リード推定 (ナポ以外の切り札リードで副官特定を早める)」は全 CANDIDATE で連合 +0.05pt
//              止まりで不採用 (副官オラクルの伸びしろ +2.93pt に対しシグナルが遅く、特定できる頃には既に手遅れ)。
//              「連合フォロー beaters 追撃の高位役札ガード (AllyFollowPlanner: 役札しか beater がないとき、絵札1枚かつ
//              非切り札絵札リーダーなら温存。役札の「もったい使い」回避)」も昇格済み (席0 Ally +0.15pt、全 CANDIDATE で
//              連合 48.90%→49.21% +0.31pt・獲得 +0.008枚。獲得ほぼ横ばいで終盤の取り回し改善が勝因)。
//              「連合の副官疑い強化 (RoleInference: 連合視点で未確定の副官候補を味方扱いせず確定味方とだけ協調)」は不採用。
//              席0 Ally は 3 seed で +0.27/+0.10/+0.29pt (平均 +0.22pt) と主指標は正だが、全席採用 (runAiStats) では連合
//              51.87%→51.81% とほぼ横ばい。1人だけ疑う「裏切り者」は協調的な味方場で honor を得取りし連合側勝利を押し込む
//              (位置依存の見かけ効果) が、全員が疑い合うと味方同士の協調 (honor 融通) が消えて献上回避と相殺する社会的ジレンマ。
//              真の伸びしろ (+2.10pt) は「全員疑う」鈍器でなく副官の的確な特定 (副官だけ疑い他2人とは協調) にあり、それは
//              行動推定を要し本実験の範囲外。教訓: 協調に関わる変更は AbCompare 席0 だけでなく全席 (runAiStats) で要確認。
enum class HeuristicVariant { BASELINE, CANDIDATE }
